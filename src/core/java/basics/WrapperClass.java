package core.java.basics;

public class WrapperClass {

	public static void main(String[] args) {
		
		Integer i = new Integer(10);
		Float j = new Float(10.45);
		Double k = new Double(45.54);
		Character l = new Character('D');
		Boolean m = new Boolean(false);
		
		System.out.println("Integer Data "+i);
		System.out.println("Float Data "+j);
		System.out.println("Double Data "+k);
		System.out.println("Character Data "+l);
		System.out.println("Boolean Data "+m);
	}

}

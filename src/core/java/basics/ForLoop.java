package core.java.basics;

public class ForLoop 
{
	public static void main(String[] args) 
{ 
		int i=0;
		System.out.println("Even Numebers");
		for (i=0; i<=20;i=i+2){
			System.out.println(i);
		}
		System.out.println("Odd Numebers");
		for(i=1;i<20;i=i+2){
			System.out.println(i);
			
		}
} 
}



